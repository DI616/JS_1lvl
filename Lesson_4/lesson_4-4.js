function isAnswer(q, event) {//функция проверки корректности введенного значения
    if (isNaN(event) || !isFinite(event)) {//Проверка, не является ли введённое значение NaN и является ли оно конечным числом
        alert('Вы ввели недопустимый символ');
        return false;//Флагу присвоено значение для повторного ввода и проверки значения
    }
    else if (event < 1 || event > q) {//Проверка, входит ли значение в указанные рамки
        alert('Ваше число выходит из допустимого диапозона');
        return false;//Флагу присвоено значение для повторного ввода и проверки значения
    }
    return true;//Если значение прошло проверку, флагу присваивается значение, позволяющее выйти из цикла проверки
    
}